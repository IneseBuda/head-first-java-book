public class WhileIfloop {
    public static void main(String[] args) {
        int x=1;
        while (x< 10){
            System.out.println("Doo" + x);
            System.out.println("Bee" + x);
            x=x+1;
        }
        if(x==10){
            System.out.println("Do" + x);
        }
    }
}
